package br.com.geekuniversity.secao18;
// Realizando Cálculos com Math - Mathematic

/*
 - A biblioteca Math possui vário métodos e constantes estáticas para que possamos utilizar nos nossos programas.
*/
public class Programa40 {

	public static void main(String[] args) {
		System.out.println(Math.E); // Constante E
		
		System.out.println(Math.PI); // Constante de PI
		
		System.out.println(Math.sin(45)); // Seno de 45°
		
		System.out.println(Math.cos(45)); // Coseno de 45°
		
		System.out.println(Math.tan(45)); // Tangente de 45°
		
		System.out.println(Math.round(Math.PI)); // Arredondando o valor repassado, nesse caso PI
		
		System.out.println(Math.ceil(Math.PI)); // Arredonda para cima o valor repassado, nesse caso PI
		
		System.out.println(Math.floor(Math.PI)); //  Arredonda para baixo o valor repassado, nesse caso PI
		
		System.out.println(Math.pow(3, 2)); // Eleva um número a outro repassado nesse caso 3 ao quadrado
		
		System.out.println(Math.round(Math.pow(3, 2))); // Eleva um número a outro repassado nesse caso 3 ao quadrado arredondada
		
		System.out.println(Math.sqrt(9)); // Calcula a raiz quadrada
		
		System.out.println(Math.round(Math.sqrt(9))); // Calcula a raiz quadrada arredondada
	}

}
